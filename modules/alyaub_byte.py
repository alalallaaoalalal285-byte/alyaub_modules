    async def bite_loop(self, target_id, chat_id, stop_event):
        while not stop_event.is_set():
            for text in self.bite_texts:
                if stop_event.is_set():
                    break
                try:
                    await self.client.send_message(chat_id, f'@{target_id} {text}')
                    await asyncio.sleep(0.3)
                except:
                    pass

    @loader.command()
    async def байтcmd(self, message: Message):
        reply = await message.get_reply_message()
        if not reply:
            await utils.answer(message, "❌ Ответь на сообщение")
            return
        target = reply.sender_id
        chat = message.chat_id
        key = f"{chat}_{target}"
        if key in self.active_bites:
            await utils.answer(message, "❌ Уже активен")
            return
        stop = asyncio.Event()
        task = asyncio.create_task(self.bite_loop(target, chat, stop))
        self.active_bites[key] = (task, stop)
        await utils.answer(message, "⚔️ Байтинг начат")

    @loader.command()
    async def байтстопcmd(self, message: Message):
        chat = message.chat_id
        stopped = False
        for key in list(self.active_bites.keys()):
            if key.startswith(f"{chat}_"):
                task, stop = self.active_bites[key]
                stop.set()
                task.cancel()
                del self.active_bites[key]
                stopped = True
        if stopped:
            await utils.answer(message, "🛑 Остановлен")
        else:
            await utils.answer(message, "❌ Нет активного байтинга")
